# Function that computes the next value of the step size. The corresponding
# formula is
# \gamma_{n}=|\frac{x_{n}-x_{n-1}}{f^{\prime}(x_{n})-f^{\prime}(x_{n-1})}|.
function gam = adapt_gamma (current_x, next_x, current_df, next_df)
  gam = abs ((next_x - current_x) / (next_df - current_df));
endfunction
