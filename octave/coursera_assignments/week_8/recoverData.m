# Function that recovers an approximation of the original data from the
# projected examples in the matrix Z. It's assumed that the reduced space is
# K-dimensional. The approximate reconstruction is returned through the m by n
# matrix X_rec.
function X_rec = recoverData (Z, U, K)

  # Computes the matrix X_rec. More precisely, calculates the approximation of
  # the original data by projecting the examples in Z back onto the original
  # space.
  X_rec = Z * U(:,1:K)';

endfunction
