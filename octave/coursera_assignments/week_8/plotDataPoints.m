# Function that plots the data points in X. The function colors the points so
# that those with the same centroid membership have the same color. This file
# isn't just a copy of the one provided to us. I changed the original code
# considerably.
function plotDataPoints (X, idx, K)

  # Color palette.
  palette = hsv (K + 1); # The hsv function creates a colormap.
  colors = palette(idx,:);

  # Plots the data.
  scatter (X(:,1), X(:,2), 15, colors);

endfunction
