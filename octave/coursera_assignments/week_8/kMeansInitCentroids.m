# This function initializes K centroids so we can use the K-means
# algorithm on the dataset X. These centroids are randomly chosen
# examples from the dataset.
function centroids = kMeansInitCentroids (X, K)
  rand_indices = randperm (rows (X));
  centroids = X(rand_indices(1:K),:);
endfunction
